package f.string;

public class Characters {

	public static void main(String[] args) {

		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
		}

		// characters are numbers
		for (char i = 'à'; i <= 'ú'; i++) {
			System.out.println(i + " - " + (int) i);
		}

		// characters are numbers
		for (char i = 'a'; i <= 'z'; i++) {
			System.out.println(i);
		}
	}

}
